class Book {

    #id;

    #title;

    #author;

    #pages;

    #read;

    //=========================================================================================================

    get id() {

        return this.#id;

    } // getId()

    //========================================================================================================

    set id(value) {

        this.#id =
            value;

    } // setId()

    //=========================================================================================================

    get title() {

        return this.#title;

    } // getTitle()

    //=========================================================================================================

    set title(value) {

        this.#title =
            value;

    } // setTitle()


    //=========================================================================================================

    get author() {

        return this.#author;

    } // getAuthor()


    //=========================================================================================================

    set author(value) {

        this.#author =
            value;

    } // setAuthor()

    //=========================================================================================================

    get pages() {

        return this.#pages;

    } // getPages()

    //=========================================================================================================

    set pages(value) {

        this.#pages =
            value;

    } // setPages()

    //=========================================================================================================

    get read() {

        return this.#read;

    } // getRead()


    //=========================================================================================================

    set read(value) {

        this.#read =
            value;

    } // setRead()

    //=========================================================================================================


    constructor(
        id,
        title,
        author,
        pages,
        read
    ) {

        this.#id =
            id;

        this.title =
            title;

        this.author =
            author;

        this.pages =
            pages;

        this.read =
            read;

    } // constructor()

    //=========================================================================================================

} // class

export { Book };